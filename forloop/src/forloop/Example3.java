package forloop;

public class Example3 {

	public static void main(String[] args) {
		
		
		String employeeNames[]= {"aaa","bbb","ccc","ddd"};
		
		
		for(int i=0;i<employeeNames.length;i++)
		{
			System.out.println(employeeNames[i]);
		}
		
		System.out.println("----------");
		
		System.out.println(employeeNames[3]);
		System.out.println("----------");
		
		System.out.println(employeeNames.length);
	}

}
